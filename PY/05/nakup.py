def udelej_nakup(kdo: str, obchod: str, nakupni_seznam: list):
    print(kdo, "udělal/a nákup a šel/šla do obchodu", obchod)
    print("nákupní seznam:", nakupni_seznam)
    print()


udelej_nakup('mamka', 'Lidl', ['jugurt', 'maslo', 'okurka'])
udelej_nakup('táta', 'Globus', ['jablko', 'banán'])
udelej_nakup('děda', 'Billa', ['chleba', 'mléko'])
